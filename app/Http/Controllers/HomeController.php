    public function index(Request $request)
    {
        $home = new Home();
        $tag = $request->input('tag');
        if(!isset($tag))
        {
            return view('home');
        }
        else
        {
        $tag = str_replace(' ','',$tag);
        $tag = str_replace('#','',$tag);
        $medias = $home->instagram($tag);
        return view('home',compact('medias'));
        }
    }

